def all_symbols():
    """全ユニーク銘柄を {sym: (name, market)} で返す。"""
    out = {}
    for m in MACRO:
        for sub in m["subs"]:
            for s, n in sub["us"]:
                out[s] = (n, "us")
            for row in sub["jp"]:
                out[row[0]] = (row[1], "jp")
            for row in sub["solo"]:
                out[row[0]] = (row[1], "jp")
    return out
